package com.sais.cashoffice.CashOfficeTest.transaction.model;

public class CustomResponseBody {
	
	private double recepitAllocId;
	private double recepitNo;
	private String message;
	private String status;
	
	
	
	public double getRecepitAllocId() {
		return recepitAllocId;
	}
	public void setRecepitAllocId(double recepitAllocId) {
		this.recepitAllocId = recepitAllocId;
	}
	public double getRecepitNo() {
		return recepitNo;
	}
	public void setRecepitNo(double recepitNo) {
		this.recepitNo = recepitNo;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	

}
